    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int i = corridas.getSelectedRow();
        if ((int) corridas.getValueAt(i, 7) == 24) {
            mini();
        } else if ((int) corridas.getValueAt(i, 7) == 40) {
            primera();
        } else {
            lujo();
        }
        JOptionPane.showMessageDialog(null, "Has seleccionado una corrida con origen en " + corridas.getValueAt(i, 1).toString() + " y destino en " + corridas.getValueAt(i, 2).toString()
                + "\n" + " programada para la fecha " + corridas.getValueAt(i, 3).toString() + " autobus de clase " + corridas.getValueAt(i, 5).toString());
    }//GEN-LAST:event_jButton2ActionPerformed

