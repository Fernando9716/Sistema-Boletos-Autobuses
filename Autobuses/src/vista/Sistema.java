    private void AgregarDestino() {
        Object origenc = origen.getSelectedItem();
        ConexionLocal();
        List<Lugares> listLugares;
        listLugares = cl.findLugaresEntities();
        for (int i = 0; i < listLugares.size(); i++) {
            if (listLugares.get(i).getLugar().toString().equals(origenc)) {
                i++;
            } else {
                destino.addItem(listLugares.get(i).getLugar());
            }
           // System.out.println(destino);
        }

    }

